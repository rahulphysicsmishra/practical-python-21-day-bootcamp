# Chapter 3: Python Data Types

#  when you assign a value, it figures out the type automatically.
#  Here are some examples of different data types in Python:

name = "Alice"       # String
age = 30             # Integer
pi = 3.14            # Float
is_student = True    # Boolean
nothing = None       # NoneType

# working with text
first = 'Hello'
second = "World"
print(first, second)   # Output: Hello World

# Use double quotes if your text has an apostrophe:
sentence = "It's a sunny day"
print(sentence)  # Output: It's a sunny day


# printing and joining strings
greeting = "Hello, Python!"
print(greeting)              # Hello, Python!

# Joining strings
name = "Rahul"
print("Hi " + name)          # Hi Rahul



# Escape characters
print("Hello\nWorld")  
# Hello
# World

print("Name:\tRahul")  
# Name:    Rahul

print("This is a backslash: \\")  
# This is a backslash: \

#  whole numbers
apples = 5
year = 2025
print(apples, year)   # 5 2025
print(10 + 5)   # 15
print(7 * 3)    # 21


# numbers with decimal points
pi = 3.14159
temperature = 36.6
print(pi, temperature)


# Boolean values
is_hungry = True
is_raining = False

print(is_hungry)      # True
print(is_raining)     # False


# None types
result = None
print(result)     # Output: None